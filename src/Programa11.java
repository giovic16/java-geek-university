
// Seção 6 - Aula 25 parte 1: Tipos numéricos Inteiros

public class Programa11 {
    public static void main(String[] args) {
        // Tipos primários/primitivos = são os tipos básicos da linguagem Java
        int num1 = 4; // Inteiro. Int cabe números inteiros maiores 999999999
        short num2 = 4; // Inteiro (short = curto/menor/baixo). Short cabe números inteiros menores 99999

        // Tipos não primários/primitivos = são classes que representa os tipos primários
        Integer num3 = 98;
        Short num4 = 7;

        System.out.println("int -> Num1 = " + num1);
        System.out.println("short -> Num2 = " + num2);
        System.out.println("Integer -> Num3 = " + num3);
        System.out.println("Short -> Num4 = " + num4);
    }
}
